

new_btn.addEventListener("click", function(){
    
    
    if (item_form.className == "item_form_show") {item_form.className = "item_form"}
    else {item_form.className = "item_form_show"}
    
})

cancel_btn.addEventListener("click", function(){
    
    
    if (item_form.className == "item_form_show") {item_form.className = "item_form"}
    
})


